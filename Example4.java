package Operator;

//Arithmetic Operator
public class Example4 {

    public static void main(String[] args) {

        int a = 10;
        int b = 50;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
