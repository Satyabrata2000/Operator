package Operator;

//Assignment operator
public class Example15 {

    public static void main(String[] args) {

        int a = 10;
        a+= 3; //a=a+3
        System.out.println(a);
        a-= 3; //a=a-4
        System.out.println(a);
        a*=2; //a=a*2
        System.out.println(a);
        a/=2; //a=a/2
        System.out.println(a);
    }
}
