package Operator;

//Unary Operator
public class Example1 {

    public static void main(String[] args) {

        int x=10;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);
        System.out.println(+x);
        System.out.println(x);
        System.out.println(++x);
        System.out.println(x);
        System.out.println(x--);
        System.out.println(x);
        System.out.println(-x);
    }
}
